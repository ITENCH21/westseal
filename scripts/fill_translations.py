po_path = "locale/en/LC_MESSAGES/django.po"
with open(po_path, encoding="utf-8") as f:
    content = f.read()

lines = content.split("\n")
out = []
i = 0
filled = 0
skipped = []
while i < len(lines):
    line = lines[i]
    if line.startswith("msgid ") and line != 'msgid ""':
        raw = line[6:]
        j = i + 1
        while j < len(lines) and lines[j].startswith('"'):
            raw += "\n" + lines[j]
            j += 1
        try:
            key = eval(raw)
        except Exception:
            key = None
        out.append(line)
        i += 1
        # collect any continuation and the msgstr
        while i < len(lines) and not lines[i].startswith("msgstr"):
            out.append(lines[i])
            i += 1
        if i < len(lines) and lines[i].startswith("msgstr"):
            if key and key in translations:
                tr = translations[key].replace("\\", "\\\\").replace('"', '\\"')
                out.append(f'msgstr "{tr}"')
                filled += 1
            else:
                if key:
                    skipped.append(key[:60])
                out.append(lines[i])
            i += 1
    else:
        out.append(line)
        i += 1

new_content = "\n".join(out)
