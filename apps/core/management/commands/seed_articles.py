        created = updated = 0
        for item in ARTICLES:
            slug = item.pop("slug")
            obj, is_new = Article.objects.get_or_create(
                slug=slug,
                defaults={"published_at": timezone.now(), "is_published": True, **item},
            )
            if is_new:
                created += 1
            else:
                for k, v in item.items():
                    setattr(obj, k, v)
                obj.save()
                updated += 1
            item["slug"] = slug  # restore for safety
        self.stdout.write(self.style.SUCCESS(
            f"Done: {created} created, {updated} updated"
        ))

